//Implement a function to sort a dynamically allocated array in ascending order using the quicksort algorithm.
void quicksort(int* arr, int L, int R) {
    if (L < R) {
        int pivot = arr[R];
        int i = L - 1;
        for (int j = L; j < R; j++) {
            if (arr[j] <= pivot) {
                i++;
                swap(arr[i], arr[j]);
            }
        }
        swap(arr[i + 1], arr[R]);

        quicksort(arr, L, i);
        quicksort(arr, i + 1, R);
    }
}


